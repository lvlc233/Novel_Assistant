# ### UI类型与渲染策略
# 1. **CONFIG类型**: 用于插件配置页面，支持树形字段结构和类型推断
# 2. **AGENT_MESSAGES类型**: 用于Agent会话管理，支持会话列表和详情展示
# 3. **CARD类型**: 用于作品和文档的卡片式展示，支持层级关系
# 4. **LIST类型**: 用于插件列表等轻量级数据展示
# 5. **DETAIL类型**: 用于文档编辑和详情展示
# 6. **DASHBOARD类型**: 用于首页仪表盘的多指标聚合视图

# ### BFF代理机制
# 所有前端数据访问都通过统一代理接口：
# - `GET /api/v1/plugin/proxy/{plugin_id}/data`
# - 后端负责鉴权、凭证注入和数据标准化
# - 前端无需直接处理数据源连接和敏感信息

# ### 插件作用域
# - **全局插件**: 记忆、项目助手、Agent管理、作品类型管理
# - **作品插件**: 知识库（每个作品独立）
# - **文档插件**: 文档创作助手（每个文档可配置）

# 这个页面功能介绍基于当前的技术架构设计和前端实现，为项目助手提供了清晰的功能描述和接口约定。